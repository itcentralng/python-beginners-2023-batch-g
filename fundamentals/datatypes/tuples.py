# Tuples are like lists, they store other datatypes seperated by
# comma. They are usually contained within parentheses brackets.

# Tuple is a way to store multiple data types within the same variable

people = ("Peter", "James", "Solomon")

people = "Peter", "James", "Solomon", "Peter"

# METHODS
print(people.count("Peter"))
print(people.index("Peter"))

# OPERATIONS
print(people[:2])
print(people[::-1])
print(people[0])