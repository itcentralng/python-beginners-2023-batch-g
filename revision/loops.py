# Loops - iteration - cycle

people = ['Ahmad', 'Mohd', 'Makarfi']

person = people[0]
print(person)
print(len(person))

person = people[1]
print(person)
print(len(person))

person = people[2]
print(person)
print(len(person))

print("\n\n")

for person in people:
    print(person)
    print(len(person))


